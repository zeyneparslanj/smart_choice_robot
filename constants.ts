// Function to generate realistic mock data for all 81 cities based on job type
const generateFullData = (job: string, baseScore: number, quotaBase: number): KpssCityData[] => {
  return TURKEY_CITIES.map(city => {
    // Determine random score variation based on region popularity (simulated)
    // Major cities (ist, ank, izm) -> higher scores
    const isMajor = ['06', '34', '35', '07', '16'].includes(city.id);
    const isEast = parseInt(city.id) > 20 && parseInt(city.id) < 80 && !isMajor; // Crude approx
    
    let scoreMod = (Math.random() * 2) - 1; // +/- 1 base variation
    if (isMajor) scoreMod += 2.5;
    if (isEast) scoreMod -= 2.0;

    let finalScore = parseFloat((baseScore + scoreMod).toFixed(2));
    // Randomly some cities might not have opened quota (10% chance)
    if (Math.random() < 0.1) finalScore = 0; 

    let quota = Math.floor(Math.random() * quotaBase) + 1;
    if (isMajor) quota *= 3;
    if (finalScore === 0) quota = 0;

    return {
      plate: city.id,
      name: city.name,
      minScore: finalScore,
      quota: quota
    };
  });
};

